    public void CargarTabla(){
        tableAviones = new DefaultTableModel();
        tableAviones.addColumn("id");
        tableAviones.addColumn("Tipo");
        tableAviones.addColumn("Combustible");
        
        LinkedList<AvionesClass> lista = data.getAviones();
        for(int i=0;i<lista.size();i++){
            tableAviones.addRow(new Object[]{lista.get(i).getId(),lista.get(i).getTipo(),lista.get(i).getCombustible()});
        }
        TablaVuelo.setModel(tableAviones);
    }
    
